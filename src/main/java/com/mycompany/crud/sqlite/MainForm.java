 public void load_table(){

           DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("Id");
        model.addColumn("Nombre");
        model.addColumn("Ip");
        model.addColumn("Mac");
        model.addColumn("Localizacion");
        model.addColumn("Notas");
        Connection con= null;

                try{
        String url = Conexion.conection_string;
            con = DriverManager.getConnection(url);

            String sql_tabla = "CREATE TABLE IF NOT EXISTS directorio ("
                    + "id integer primary key,"
                    + "name text primary key,"
                    + "ipd text primary key,"
                    + "macd text primary key,"
                    + "location text,"
                    + "notes text"
                    + ");";
            
            Statement stmt = con.createStatement();
            stmt.execute(sql_tabla);
            
                        String sql_select = "select * from directorio";
            Statement stmt2 = con.createStatement();
            ResultSet rs = stmt2.executeQuery(sql_select);
            
            while(rs.next()){
                        model.addRow(new Object[]{rs.getString("id"),rs.getString("name"),rs.getString("ipd"),rs.getString("macd"),rs.getString("location"), rs.getString("notes")});

                System.out.println("Id:" + rs.getString("ipd"));
                System.out.println("Nombre:" + rs.getString("name"));
                System.out.println("Ip:" + rs.getString("ipd"));
                System.out.println("Notas:" + rs.getString("notes"));
                System.out.println("Localizacion:" + rs.getString("location"));
                 System.out.println("Mac:" + rs.getString("macd"));
            }
            
                    jTable1.setModel(model);


        }
        catch(SQLException e){
            System.out.println("Error:" + e.getMessage());
        
        }

}

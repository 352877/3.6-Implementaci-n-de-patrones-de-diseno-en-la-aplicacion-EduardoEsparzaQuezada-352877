    private void eguardarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eguardarButtonActionPerformed
        // TODO add your handling code here:
Connection con= null;
        try{
        String url = Conexion.conection_string;
            con = DriverManager.getConnection(url);

            String sql_insert = "insert into directorio (name, ipd, macd, location, notes) values (?,?,?,?,?)";
            
            PreparedStatement pstmt = con.prepareStatement(sql_insert);
            pstmt.setString(1, name_text.getText());
            pstmt.setString(2, ipd_text.getText());
            pstmt.setString(3, macd_text.getText());
            pstmt.setString(4, location_text.getText());
            pstmt.setString(5, notes_text.getText());
            
            pstmt.executeUpdate();
            
            name_text.setText("");
            ipd_text.setText("");
            macd_text.setText("");
            location_text.setText("");
            notes_text.setText("");
        }
        catch(SQLException e){
            System.out.println("Error:" + e.getMessage());
        
        }



    }//GEN-LAST:event_eguardarButtonActionPerformed

